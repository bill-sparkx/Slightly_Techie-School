/*1. Print Numbers (Basic)
Objective: Practice printing using all 3 loop types
Print numbers from 1 to 10 using:
a for loop
a while loop
a do...while loop
*/
//for loop
for (let i = 1; i <=10; i++ ){
    console.log(i)
}

//While loop
let a=1;
while(a <= 10){
    console.log(a)
    a++;
}

//do while
let k=1;
do{
    console.log(k);
    k++;
}while (k <=10);